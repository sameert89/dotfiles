			"",
			"template <> struct Deserialize<TreeNode *> {",
			"    static TreeNode *parse(const string &s) {",
			"        string inner = strip_brackets(s);",
			"        vector<string> toks = split_top_level(inner);",
			"        if (toks.empty() || is_null_token(toks[0])) return nullptr;",
