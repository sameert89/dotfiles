			"",
			"vector<string> split_lines(const string &block) {",
			"    vector<string> out;",
			"    istringstream in(block);",
			"    string line;",
			"    while (getline(in, line)) {",
			"        line = trim(line);",
			"        if (!line.empty()) out.push_back(line);",
			"    }",
			"    return out;",
			"}",
			"",
			"vector<string> split_top_level(const string &s) {",
			"    vector<string> out;",
			"    string cur;",
			"    int sq = 0;",
			"    bool in_str = false;",
			"    for (size_t i = 0; i < s.size(); ++i) {",
			"        char ch = s[i];",
			"        if (ch == '\"' && (i == 0 || s[i - 1] != '\\\\')) in_str = !in_str;",
			"        if (!in_str) {",
			"            if (ch == '[') ++sq;",
			"            else if (ch == ']') --sq;",
			"            else if (ch == ',' && sq == 0) {",
			"                out.push_back(trim(cur));",
			"                cur.clear();",
			"                continue;",
			"            }",
			"        }",
			"        cur.push_back(ch);",
			"    }",
			"    if (!cur.empty()) out.push_back(trim(cur));",
			"    if (out.size() == 1 && out[0].empty()) out.clear();",
			"    return out;",
			"}",
			"",
			"string strip_brackets(const string &s) {",
			"    string t = trim(s);",
			"    if (t.size() < 2 || t.front() != '[' || t.back() != ']') throw runtime_error(\"bad list: \" + t);",
			"    return trim(t.substr(1, t.size() - 2));",
			"}",
			"",
			"bool is_null_token(const string &s) {",
