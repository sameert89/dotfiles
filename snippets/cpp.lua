			"    for (size_t i = 0; i < v.size(); ++i) {",
			"        if (i) out += \",\";",
			"        out += serialize(v[i]);",
			"    }",
			"    out += \"]\";",
			"    return out;",
			"}",
			"",
			"template <> string serialize(ListNode *const &head) {",
			"    vector<int> vals;",
			"    for (ListNode *p = head; p != nullptr; p = p->next) vals.push_back(p->val);",
			"    return serialize(vals);",
			"}",
			"",
			"template <> string serialize(TreeNode *const &root) {",
			"    if (root == nullptr) return \"[]\";",
			"    vector<string> vals;",
			"    queue<TreeNode *> q;",
			"    q.push(root);",
			"    while (!q.empty()) {",
			"        TreeNode *cur = q.front();",
			"        q.pop();",
			"        if (cur == nullptr) {",
			"            vals.push_back(\"null\");",
			"            continue;",
			"        }",
			"        vals.push_back(to_string(cur->val));",
			"        q.push(cur->left);",
			"        q.push(cur->right);",
			"    }",
			"    while (!vals.empty() && vals.back() == \"null\") vals.pop_back();",
			"    string out = \"[\";",
			"    for (size_t i = 0; i < vals.size(); ++i) {",
			"        if (i) out += \",\";",
			"        out += vals[i];",
			"    }",
			"    out += \"]\";",
			"    return out;",
			"}",
			"",
