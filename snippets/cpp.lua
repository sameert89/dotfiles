			"",
			"template <> string serialize<int>(const int &v) { return to_string(v); }",
			"template <> string serialize<long long>(const long long &v) { return to_string(v); }",
			"template <> string serialize<bool>(const bool &v) { return v ? \"true\" : \"false\"; }",
			"template <> string serialize<string>(const string &v) { return \"\\\"\" + v + \"\\\"\"; }",
