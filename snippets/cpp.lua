			"    static_assert(arity > 0, \"InputTuple cannot be empty\");",
			"    if (lines.size() % arity != 0) throw runtime_error(\"input lines not divisible by InputTuple arity\");",
			"    vector<Tuple> out;",
			"    for (size_t i = 0; i < lines.size(); i += arity) {",
			"        out.push_back(parse_input_case_impl<Tuple>(lines, i, make_index_sequence<arity>{}));",
			"    }",
			"    return out;",
			"}",
			"",
			"template <typename Tuple, size_t... I>",
			"Tuple parse_output_line_impl(const string &line, index_sequence<I...>) {",
			"    constexpr size_t arity = sizeof...(I);",
			"    static_assert(arity > 0, \"OutputTuple cannot be empty\");",
