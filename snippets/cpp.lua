			"            if (toks.size() != 1) throw;",
			"            return Tuple{parse_value<Only>(toks[0])};",
			"        }",
			"    }",
			"    auto toks = split_top_level(strip_brackets(line));",
			"    if (toks.size() != arity) throw runtime_error(\"output arity mismatch in line: \" + line);",
			"    return Tuple{parse_value<tuple_element_t<I, Tuple>>(toks[I])...};",
			"}",
			"",
			"template <typename Tuple> vector<Tuple> parse_output_cases(const string &block) {",
			"    vector<Tuple> out;",
			"    for (const auto &line : split_lines(block)) {",
			"        out.push_back(parse_output_line_impl<Tuple>(line, make_index_sequence<tuple_size_v<Tuple>>{}));",
			"    }",
			"    return out;",
			"}",
			"",
			"class Solution {",
